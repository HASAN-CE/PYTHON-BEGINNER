student_marks = int(input('Enter your marks: '))

if(90<=student_marks<100):
    print('Grade: Ex')
elif(80<=student_marks<90):
    print('Grade: A')
elif(70<=student_marks<80):
    print('Grade: B')
elif(60<=student_marks<70):
    print('Grade: C')
elif(50<=student_marks<60):
    print('Grade: C')
else:
    print('Grade: F')
